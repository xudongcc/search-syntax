/**
 * Comparison operator types used in search queries.
 */
export enum Comparator {
  /** Equal comparison */
  EQ = "EQ",
  /** Less than comparison */
  LT = "LT",
  /** Greater than comparison */
  GT = "GT",
  /** Less than or equal comparison */
  LE = "LE",
  /** Greater than or equal comparison */
  GE = "GE",
}
